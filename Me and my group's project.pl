logos will represent are Albright's athletics program, Public Safety and the school of Albright College itself. These logos will be represented
in the form of a slideshow; in other words, the logos will be stacked ontop of one another and move down the screen so that the viewer can see 
every single one. There will also be backrounds for these logos consisting of red and white, representing the colors of Albright itself. That 
is the basics of what me and my group's code will be like.

